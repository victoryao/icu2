
package com.xiaomi.xms.sales.model;

public class AddToShppingCartInfo {

    public String mAllProductId;
    public String mProductId;
    public String mConsumption;
    public String mPromotionId;
    public String mPromotionType;
    public String mSecurityCode;

    public void setAllProductId(String allProductId) {
        this.mAllProductId = allProductId;
    }

    public void setProductId(String productId) {
        this.mProductId = productId;
    }

    public void setConsumption(String consumption) {
        this.mConsumption = consumption;
    }

    public void setPromotionId(String promotionId) {
        this.mPromotionId = promotionId;
    }

    public void setPromotionType(String promotionType) {
        this.mPromotionType = promotionType;
    }

    public void setSecurityCode(String securityCode) {
        this.mSecurityCode = securityCode;
    }

}
